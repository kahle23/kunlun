package kunlun.logging;

/**
 * The logger level.
 * @author Kahle
 */
public enum Level {

    /**
     * The level trace.
     */
    TRACE,

    /**
     * The level debug.
     */
    DEBUG,

    /**
     * The level info.
     */
    INFO,

    /**
     * The level warn.
     */
    WARN,

    /**
     * The level error.
     */
    ERROR

}
