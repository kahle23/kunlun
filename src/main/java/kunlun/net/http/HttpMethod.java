package kunlun.net.http;

/**
 * The http method.
 * @author Kahle
 */
public enum HttpMethod {

    /**
     * The http get method.
     */
    GET,

    /**
     * The http post method.
     */
    POST,

    /**
     * The http put method.
     */
    PUT,

    /**
     * The http delete method.
     */
    DELETE,

    /**
     * The http head method.
     */
    HEAD,

    /**
     * The http options method.
     */
    OPTIONS,

    /**
     * The http trace method.
     */
    TRACE,

    /**
     * The http connect method.
     */
    CONNECT,

    /**
     * The http patch method.
     */
    PATCH,
    ;

}
